export const getDaysAndHours = (startDate, endDate) => {
  const start = new Date(startDate);
  const startDay = start.getDay();
  const startHour = start.getHours();
  const end = new Date(endDate);
  const endHour = end.getHours();
  const endDay = end.getDay();
  const diffDays = (end.getDay() || 7) - start.getDay();
  if (diffDays < 0) {
    throw new Error('End date is before start date');
  }
  if (diffDays === 0) {
    return lookUpObject[startDay].filter(hour => hour.hour >= startHour && hour.hour <= endHour)
  } else if (diffDays === 1) {
    return lookUpObject[startDay].filter(hour => hour.hour >= startHour).concat(lookUpObject[endDay].filter(hour => hour.hour <= endHour))
  } else {
    const daysHours = [];
    daysHours.push(lookUpObject[startDay].filter(hour => hour.hour >= startHour));
    daysHours.push(lookUpObject[endDay].filter(hour => hour.hour <= endHour));
    while (start.getDay() + 1 <= (end.getDay() || 7) - 1) {
      start.setDate(start.getDate() + 1);
      daysHours.push(lookUpObject[start.getDay()]);
    }
    return daysHours.reduce((acc, curr) => acc.concat(curr), []);
  }
}